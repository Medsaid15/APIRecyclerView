package com.example.apirecyclerview;

public class PlaceholderPost {
    private int userID;
    private int id;
    private String title;
    private String body;

    public PlaceholderPost() {
    }

    public int getUserId() {
        return userID;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getBody() {
        return body;
    }

}
